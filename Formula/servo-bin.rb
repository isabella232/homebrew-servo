class ServoBin < Formula
  desc "Servo, the Parallel Browser Engine Project (binary version)"
  homepage "http://servo.org"
  url "https://download.servo.org/nightly/macbrew/2017-04-13T01-08-26Z-servo.tar.gz"
  version "2017.04.13"
  sha256 "486cb92c2ab75876c0c14815d7e4fb13e2a221813b7f47691c058ef70c20bc12"

  bottle :unneeded

  def install
    prefix.install "bin", "libexec", "resources"
  end

  test do
    system bin/"servo", "-o", "/dev/null", "-x", "http://example.com"
  end
end
